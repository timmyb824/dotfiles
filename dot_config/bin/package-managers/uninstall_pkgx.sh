# Function to uninstall a package
uninstall_package() {
    local package=$1
    echo_with_color "34" "Uninstalling ${package}..."
    if pkgx uninstall "${package}"; then
        echo_with_color "32" "${package} uninstalled successfully."
    else
        echo_with_color "31" "Failed to uninstall ${package}."
